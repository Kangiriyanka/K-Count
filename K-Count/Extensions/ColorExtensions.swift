import SwiftUI

extension Color {
    
    static public var customGreen: Color {
        Color.init(Color(red: 0.0039, green: 0.4196, blue: 0.2392))
    }
    
 
    static public var  burntOrange : Color {
        Color.init(Color(red: 204 / 255, green: 85 / 255, blue: 0 / 255))
    }
    
    static public var emeraldGreen: Color {
        Color(Color(red: 0 / 255, green: 103 / 255, blue: 79 / 255))
    }
    
}


